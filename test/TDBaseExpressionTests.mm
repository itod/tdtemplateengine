#import "TDBaseExpressionTests.h"

@implementation TDBaseExpressionTests

- (void)setUp {
    [super setUp];
    
    self.eng = [TDTemplateEngine templateEngine]; // create thread local temp engine
    self.expr = nil;
}

- (void)tearDown {
    self.expr = nil;
    self.eng = nil;
    
    [super tearDown];
}

- (NSArray *)tokenize:(NSString *)input {
    PKTokenizer *t = [TDParser tokenizer];
    t.string = input;
    
    PKToken *tok = nil;
    PKToken *eof = [PKToken EOFToken];
    
    NSMutableArray *toks = [NSMutableArray array];
    
    while (eof != (tok = [t nextToken])) {
        [toks addObject:tok];
    }
    return toks;
}

@end
